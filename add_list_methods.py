# insert() method
b=["house","car","money","fame"]
b.insert(3,"Happy")    # we can insert items into the list using the insert()
print(b)


# append() method
b1=["house","car","money","fame"]
b1.append("Birthday")   #the append() method will add the item at the end of the list
print(b1)


# extend() method
a=[1,2,3,4]
b=["house","car","money","fame"]
a.extend(b)    #the extend() method adds the 2nd list to the end of the 1st list
print(a)

